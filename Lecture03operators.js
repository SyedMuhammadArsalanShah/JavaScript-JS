num1=parseInt(prompt("Enter Your  1st Number"))//2
num2=parseInt( prompt("Enter Your 2nd Number"))//2

answeradd=num1+num2//2+2= 4
answersub=num1-num2
answermul=num1*num2
answerdiv=num1/num2
answermod=num1%num2

document.write( num1 + "+"    +  num2 + "="+ answeradd+"<br>")
document.write( num1 + "-"    +  num2 + "="+ answersub+"<br>")
document.write( num1 + "*"    +  num2 + "="+ answermul+"<br>")
document.write( num1  + "/"   +  num2 + "="+ answerdiv+"<br>")
document.write( num1 + "%"    +  num2 + "="+ answermod+"<br>")